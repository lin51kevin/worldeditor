/// Parse a SUMO `shape` attribute (`"x1,y1 x2,y2 ..."`) into points.
fn parse_shape(shape: &str) -> Vec<(f64, f64)> {
    shape
        .split_whitespace()
        .filter_map(|pair| {
            let mut it = pair.split(',');
            let x = it.next()?.parse::<f64>().ok()?;
            let y = it.next()?.parse::<f64>().ok()?;
            Some((x, y))
        })
        .collect()
}

/// Format points back into a SUMO `shape` string.
fn format_shape(points: &[(f64, f64)]) -> String {
    points
        .iter()
        .map(|(x, y)| format!("{x:.3},{y:.3}"))
        .collect::<Vec<_>>()
        .join(" ")
}

/// Average several equal-length lane shapes into a centerline, or fall back to
/// the middle lane shape when lengths differ.
fn average_lane_shapes(shapes: &[Vec<(f64, f64)>]) -> Option<Vec<(f64, f64)>> {
    let usable: Vec<&Vec<(f64, f64)>> = shapes.iter().filter(|s| s.len() >= 2).collect();
    if usable.is_empty() {
        return None;
    }
    let len = usable[0].len();
    if usable.iter().all(|s| s.len() == len) {
        let n = usable.len() as f64;
        let avg = (0..len)
            .map(|i| {
                let (sx, sy) = usable
                    .iter()
                    .fold((0.0, 0.0), |(ax, ay), s| (ax + s[i].0, ay + s[i].1));
                (sx / n, sy / n)
            })
            .collect();
        Some(avg)
    } else {
        Some(usable[usable.len() / 2].clone())
    }
}

/// Convert a polyline into a chain of OpenDRIVE line geometries.
fn polyline_to_geometries(points: &[(f64, f64)]) -> Vec<Geometry> {
    let mut geometries = Vec::new();
    let mut s = 0.0;
    for window in points.windows(2) {
        let (x0, y0) = window[0];
        let (x1, y1) = window[1];
        let (dx, dy) = (x1 - x0, y1 - y0);
        let length = (dx * dx + dy * dy).sqrt();
        if length < 1e-9 {
            continue;
        }
        geometries.push(Geometry {
            s,
            x: x0,
            y: y0,
            hdg: dy.atan2(dx),
            length,
            geo_type: GeometryType::Line,
        });
        s += length;
    }
    geometries
}

/// Recover the reference-line polyline (geometry vertices + final endpoint).
fn reference_polyline(road: &Road) -> Vec<(f64, f64)> {
    let mut points: Vec<(f64, f64)> = road.plan_view.iter().map(|g| (g.x, g.y)).collect();
    if let Some(last) = road.plan_view.last() {
        let end = evaluate_geometry(last, last.length);
        points.push((end.x, end.y));
    }
    points
}

/// Build a right-side driving lane with a constant width.
fn driving_lane(id: i32, width: f64) -> Lane {
    Lane {
        id,
        lane_type: LaneType::Driving,
        level: 0,
        render_hidden: false,
        link: None,
        width: vec![LaneWidth {
            s_offset: 0.0,
            a: width,
            b: 0.0,
            c: 0.0,
            d: 0.0,
        }],
        borders: Vec::new(),
        road_marks: Vec::new(),
    }
}

/// Build the mandatory zero-width center lane (id 0).
fn center_lane() -> Lane {
    Lane {
        id: 0,
        lane_type: LaneType::None,
        level: 0,
        render_hidden: false,
        link: None,
        width: Vec::new(),
        borders: Vec::new(),
        road_marks: Vec::new(),
    }
}

/// Axis-aligned bounds of all points (defaults to origin when empty).
fn bounds(points: &[(f64, f64)]) -> (f64, f64, f64, f64) {
    points.iter().fold(
        (0.0, 0.0, 0.0, 0.0),
        |(min_x, min_y, max_x, max_y), &(x, y)| {
            (min_x.min(x), min_y.min(y), max_x.max(x), max_y.max(y))
        },
    )
}

/// Read an attribute value as an owned `String`.
fn get_attr(e: &BytesStart, key: &[u8]) -> Option<String> {
    e.attributes()
        .flatten()
        .find(|a| a.key.as_ref() == key)
        .and_then(|a| a.unescape_value().ok().map(|c| c.into_owned()))
}

/// Minimal XML attribute escaping for exported identifiers.
fn xml_escape(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
}

#[cfg(test)]
mod tests {
    use super::*;

    const SAMPLE_NET: &str = r#"<?xml version="1.0" encoding="UTF-8"?>
<net version="1.16">
  <location netOffset="0.00,0.00" convBoundary="0.00,0.00,100.00,0.00" origBoundary="0,0,100,0" projParameter="!"/>
  <edge id="e1" from="A" to="B" priority="1" shape="0.00,0.00 50.00,0.00 100.00,0.00">
    <lane id="e1_0" index="0" speed="13.89" length="100.00" width="3.20" shape="0,-1.6 100,-1.6"/>
    <lane id="e1_1" index="1" speed="13.89" length="100.00" width="3.50" shape="0,1.6 100,1.6"/>
  </edge>
  <edge id=":J0_0" function="internal">
    <lane id=":J0_0_0" index="0" speed="5" length="2" shape="100,0 102,0"/>
  </edge>
  <junction id="A" type="priority" x="0.00" y="0.00" incLanes="" intLanes=""/>
</net>
"#;

    #[test]
    fn parse_shape_reads_coordinate_pairs() {
        let pts = parse_shape("0.0,1.0 2.5,-3.5 10,10");
        assert_eq!(pts, vec![(0.0, 1.0), (2.5, -3.5), (10.0, 10.0)]);
    }

    #[test]
    fn empty_input_is_an_error() {
        assert!(import_sumo_net("   ").is_err());
    }

    #[test]
    fn import_parses_edges_and_skips_internal() {
        let project = import_sumo_net(SAMPLE_NET).unwrap();
        assert_eq!(project.roads.len(), 1, "internal edge must be skipped");
        let road = &project.roads[0];
        assert_eq!(road.id, "e1");
        // Reference line: three vertices -> two line segments, total length 100.
        assert_eq!(road.plan_view.len(), 2);
        assert!((road.length - 100.0).abs() < 1e-6);
        assert!((road.plan_view[0].x).abs() < 1e-9);
        assert!((road.plan_view[1].x - 50.0).abs() < 1e-9);
    }

    #[test]
    fn import_reads_lane_count_and_widths() {
        let project = import_sumo_net(SAMPLE_NET).unwrap();
        let section = &project.roads[0].lane_sections[0];
        assert_eq!(section.right.len(), 2);
        assert!(section.left.is_empty());
        // index 0 -> id -1 width 3.2, index 1 -> id -2 width 3.5
        let lane_m1 = section.right.iter().find(|l| l.id == -1).unwrap();
        let lane_m2 = section.right.iter().find(|l| l.id == -2).unwrap();
        assert!((lane_m1.width[0].a - 3.2).abs() < 1e-9);
        assert!((lane_m2.width[0].a - 3.5).abs() < 1e-9);
    }

    #[test]
    fn export_emits_location_edge_and_junctions() {
        let project = import_sumo_net(SAMPLE_NET).unwrap();
        let xml = export_sumo_net(&project).unwrap();
        assert!(xml.contains("<location"));
        assert!(xml.contains("<edge id=\"e1\""));
        assert!(xml.contains("<lane id=\"e1_0\""));
        assert!(xml.contains("<junction id=\"e1_start\""));
        assert!(xml.contains("</net>"));
    }

    #[test]
    fn export_then_import_round_trips_geometry_and_lanes() {
        let original = import_sumo_net(SAMPLE_NET).unwrap();
        let xml = export_sumo_net(&original).unwrap();
        let reimported = import_sumo_net(&xml).unwrap();

        assert_eq!(reimported.roads.len(), original.roads.len());
        let a = &original.roads[0];
        let b = &reimported.roads[0];

        // Geometry polyline preserved.
        let pa = reference_polyline(a);
        let pb = reference_polyline(b);
        assert_eq!(pa.len(), pb.len());
        for ((ax, ay), (bx, by)) in pa.iter().zip(pb.iter()) {
            assert!((ax - bx).abs() < 1e-2, "x {ax} != {bx}");
            assert!((ay - by).abs() < 1e-2, "y {ay} != {by}");
        }

        // Lane count + widths preserved (ordered by id -1, -2, ...).
        let mut wa: Vec<f64> = a.lane_sections[0]
            .right
            .iter()
            .map(|l| l.width[0].a)
            .collect();
        let mut wb: Vec<f64> = b.lane_sections[0]
            .right
            .iter()
            .map(|l| l.width[0].a)
            .collect();
        wa.sort_by(|x, y| x.partial_cmp(y).unwrap());
        wb.sort_by(|x, y| x.partial_cmp(y).unwrap());
        assert_eq!(wa.len(), wb.len());
        for (x, y) in wa.iter().zip(wb.iter()) {
            assert!((x - y).abs() < 1e-2, "width {x} != {y}");
        }
    }

    #[test]
    fn edge_without_shape_uses_lane_shapes() {
        let net = r#"<net version="1.16">
  <edge id="e2" from="A" to="B">
    <lane id="e2_0" index="0" speed="10" length="10" width="3.0" shape="0,0 10,0"/>
  </edge>
</net>"#;
        let project = import_sumo_net(net).unwrap();
        assert_eq!(project.roads.len(), 1);
        let road = &project.roads[0];
        assert_eq!(road.plan_view.len(), 1);
        assert!((road.length - 10.0).abs() < 1e-6);
        assert_eq!(road.lane_sections[0].right.len(), 1);
    }

    #[test]
    fn network_without_drivable_edges_errors() {
        let net = r#"<net version="1.16">
  <edge id=":J_0" function="internal">
    <lane id=":J_0_0" index="0" speed="5" length="2" shape="0,0 2,0"/>
  </edge>
</net>"#;
        assert!(import_sumo_net(net).is_err());
    }
}
