//! SUMO traffic simulator network I/O.
//!
//! Imports SUMO `.net.xml` files and exports [`Project`]s back to the SUMO
//! network format. SUMO networks use a flat Cartesian coordinate system (metres)
//! and describe roads as directed `<edge>` elements containing one or more
//! `<lane>` children, joined at `<junction>` nodes.
//!
//! ## Mapping
//!
//! - Each non-internal `<edge>` becomes a [`Road`]. The road reference line is
//!   taken from the edge `shape` polyline (falling back to the lane shapes).
//! - Edge lanes become OpenDRIVE right-side driving lanes: SUMO lane `index = i`
//!   maps to lane id `-(i + 1)`, preserving lane count and per-lane width.
//! - Internal edges/junctions (id starting with `:` or `function="internal"`)
//!   are skipped — they are regenerated by SUMO's `netconvert`.
//!
//! ## Limitations
//!
//! Junction connectivity and traffic-light programs are not yet round-tripped;
//! geometry, lane counts, lane widths and speeds are.

use quick_xml::Reader;
use quick_xml::events::{BytesStart, Event};
use we_core::geometry::eval::evaluate_geometry;
use we_core::model::{
    Geometry, GeometryType, Lane, LaneSection, LaneType, LaneWidth, Project, Road,
};

/// Default lane width (metres) when a SUMO lane omits its `width` attribute.
const DEFAULT_LANE_WIDTH: f64 = 3.2;
/// Default lane speed (m/s) used on export when a road has no speed.
const DEFAULT_SPEED: f64 = 13.89;

/// Error type for SUMO I/O operations.
#[derive(Debug, thiserror::Error)]
pub enum SumoError {
    /// The input could not be parsed as a SUMO network.
    #[error("invalid SUMO network: {0}")]
    Parse(String),
}

// ---------------------------------------------------------------------------
// Import
// ---------------------------------------------------------------------------

/// Accumulates a single SUMO lane while streaming.
struct LaneAcc {
    index: i32,
    width: f64,
    speed: f64,
}

/// Accumulates a single SUMO edge while streaming.
struct EdgeAcc {
    id: String,
    internal: bool,
    shape: Vec<(f64, f64)>,
    lane_shapes: Vec<Vec<(f64, f64)>>,
    lanes: Vec<LaneAcc>,
}

/// Import a SUMO `.net.xml` document and convert it to a [`Project`].
pub fn import_sumo_net(xml: &str) -> Result<Project, SumoError> {
    if xml.trim().is_empty() {
        return Err(SumoError::Parse("empty input".into()));
    }

    let mut reader = Reader::from_str(xml);
    let mut edges: Vec<EdgeAcc> = Vec::new();
    let mut current: Option<EdgeAcc> = None;

    loop {
        match reader
            .read_event()
            .map_err(|e| SumoError::Parse(e.to_string()))?
        {
            Event::Eof => break,
            Event::Start(e) => match e.name().as_ref() {
                b"edge" => current = Some(start_edge(&e)),
                b"lane" => add_lane(current.as_mut(), &e),
                _ => {}
            },
            Event::Empty(e) => match e.name().as_ref() {
                b"edge" => edges.push(start_edge(&e)),
                b"lane" => add_lane(current.as_mut(), &e),
                _ => {}
            },
            Event::End(e) => {
                if e.name().as_ref() == b"edge"
                    && let Some(edge) = current.take()
                {
                    edges.push(edge);
                }
            }
            _ => {}
        }
    }

    let roads: Vec<Road> = edges
        .into_iter()
        .filter(|e| !e.internal)
        .filter_map(edge_to_road)
        .collect();

    if roads.is_empty() {
        return Err(SumoError::Parse(
            "no drivable (non-internal) edges found".into(),
        ));
    }

    Ok(Project {
        name: "SUMO Import".into(),
        roads,
        ..Default::default()
    })
}

/// Begin accumulating an edge from its start/empty tag attributes.
fn start_edge(e: &BytesStart) -> EdgeAcc {
    let id = get_attr(e, b"id").unwrap_or_default();
    let function = get_attr(e, b"function").unwrap_or_default();
    let internal = function == "internal" || id.starts_with(':');
    let shape = get_attr(e, b"shape")
        .map(|s| parse_shape(&s))
        .unwrap_or_default();
    EdgeAcc {
        id,
        internal,
        shape,
        lane_shapes: Vec::new(),
        lanes: Vec::new(),
    }
}

/// Attach a lane (and its shape) to the in-progress edge, if any.
fn add_lane(edge: Option<&mut EdgeAcc>, e: &BytesStart) {
    let Some(edge) = edge else {
        return;
    };
    let index = get_attr(e, b"index")
        .and_then(|v| v.parse::<i32>().ok())
        .unwrap_or(edge.lanes.len() as i32);
    let width = get_attr(e, b"width")
        .and_then(|v| v.parse::<f64>().ok())
        .filter(|w| *w > 0.0)
        .unwrap_or(DEFAULT_LANE_WIDTH);
    let speed = get_attr(e, b"speed")
        .and_then(|v| v.parse::<f64>().ok())
        .unwrap_or(DEFAULT_SPEED);
    if let Some(shape) = get_attr(e, b"shape") {
        edge.lane_shapes.push(parse_shape(&shape));
    }
    edge.lanes.push(LaneAcc {
        index,
        width,
        speed,
    });
}

/// Convert an accumulated edge into a [`Road`], or `None` if it has no geometry.
fn edge_to_road(mut edge: EdgeAcc) -> Option<Road> {
    let centerline = if edge.shape.len() >= 2 {
        edge.shape.clone()
    } else {
        average_lane_shapes(&edge.lane_shapes)?
    };

    let geometries = polyline_to_geometries(&centerline);
    if geometries.is_empty() {
        return None;
    }
    let length: f64 = geometries.iter().map(|g| g.length).sum();

    let mut road = Road::new(edge.id.clone(), length);
    road.plan_view = geometries;
    road.speed = edge
        .lanes
        .iter()
        .map(|l| l.speed)
        .fold(None, |acc, s| Some(acc.map_or(s, |a: f64| a.max(s))));

    // Ensure deterministic lane order by SUMO index.
    edge.lanes.sort_by_key(|l| l.index);
    if edge.lanes.is_empty() {
        edge.lanes.push(LaneAcc {
            index: 0,
            width: DEFAULT_LANE_WIDTH,
            speed: DEFAULT_SPEED,
        });
    }

    let right: Vec<Lane> = edge
        .lanes
        .iter()
        .enumerate()
        .map(|(i, lane)| driving_lane(-(i as i32 + 1), lane.width))
        .collect();

    road.lane_sections.push(LaneSection {
        s: 0.0,
        single_side: false,
        render_hidden: false,
        left: Vec::new(),
        center: vec![center_lane()],
        right,
    });

    Some(road)
}

