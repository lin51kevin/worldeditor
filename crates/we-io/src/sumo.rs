/// Export a [`Project`] to SUMO `.net.xml` text.
pub fn export_sumo_net(project: &Project) -> Result<String, SumoError> {
    let mut all_points: Vec<(f64, f64)> = Vec::new();
    let mut edges = String::new();
    let mut junctions = String::new();

    for road in &project.roads {
        let polyline = reference_polyline(road);
        if polyline.len() < 2 {
            continue;
        }
        all_points.extend_from_slice(&polyline);

        let shape = format_shape(&polyline);
        let speed = road.speed.unwrap_or(DEFAULT_SPEED);
        let length = road.length;
        let from = format!("{}_start", road.id);
        let to = format!("{}_end", road.id);

        edges.push_str(&format!(
            "  <edge id=\"{}\" from=\"{}\" to=\"{}\" priority=\"-1\" shape=\"{}\">\n",
            xml_escape(&road.id),
            xml_escape(&from),
            xml_escape(&to),
            shape,
        ));
        for (index, lane) in export_lane_widths(road).into_iter().enumerate() {
            edges.push_str(&format!(
                "    <lane id=\"{}_{}\" index=\"{}\" speed=\"{:.2}\" length=\"{:.2}\" width=\"{:.2}\" shape=\"{}\"/>\n",
                xml_escape(&road.id),
                index,
                index,
                speed,
                length,
                lane,
                shape,
            ));
        }
        edges.push_str("  </edge>\n");

        let (sx, sy) = polyline[0];
        let (ex, ey) = *polyline.last().unwrap();
        junctions.push_str(&format!(
            "  <junction id=\"{}\" type=\"priority\" x=\"{:.2}\" y=\"{:.2}\" incLanes=\"\" intLanes=\"\" shape=\"\"/>\n",
            xml_escape(&from), sx, sy,
        ));
        junctions.push_str(&format!(
            "  <junction id=\"{}\" type=\"priority\" x=\"{:.2}\" y=\"{:.2}\" incLanes=\"\" intLanes=\"\" shape=\"\"/>\n",
            xml_escape(&to), ex, ey,
        ));
    }

    let (min_x, min_y, max_x, max_y) = bounds(&all_points);

    let mut out = String::new();
    out.push_str("<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n");
    out.push_str("<net version=\"1.16\" junctionCornerDetail=\"5\" limitTurnSpeed=\"5.50\">\n");
    out.push_str(&format!(
        "  <location netOffset=\"0.00,0.00\" convBoundary=\"{:.2},{:.2},{:.2},{:.2}\" origBoundary=\"{:.2},{:.2},{:.2},{:.2}\" projParameter=\"!\"/>\n",
        min_x, min_y, max_x, max_y, min_x, min_y, max_x, max_y,
    ));
    out.push_str(&edges);
    out.push_str(&junctions);
    out.push_str("</net>\n");
    Ok(out)
}

/// Lane widths to export, ordered to match SUMO lane indices (0 = id `-1`).
fn export_lane_widths(road: &Road) -> Vec<f64> {
    let Some(section) = road.lane_sections.first() else {
        return vec![3.5];
    };
    let mut lanes: Vec<&Lane> = section
        .right
        .iter()
        .filter(|l| l.lane_type.is_driving())
        .collect();
    // id -1 -> index 0, id -2 -> index 1, ...
    lanes.sort_by_key(|l| -l.id);
    if lanes.is_empty() {
        return vec![3.5];
    }
    lanes
        .iter()
        .map(|l| l.width.first().map(|w| w.a).unwrap_or(3.5))
        .collect()
}

